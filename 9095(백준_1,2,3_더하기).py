import sys
input = sys.stdin.readline

n = int(input())
'''
1 -> (1)
2 -> (1 1), (2)
3 -> (1, 1, 1) (2, 1), (1, 2), (3)
4 -> (1, 1, 1, 1), (2, 1, 1), (1, 2, 1), (1, 1, 2), (3, 1), (1, 3)
5 -> (1, 1, 1, 1, 1), (2, 1, 1, 1) * 4, (2, 2, 1) * 3, (3, 1, 1) * 3, (3, 2), (2, 3)
1, 2, 4, 7, 13 --- 
'''
dp = [0, 1, 2, 4] + [0] * 8
for i in range(4, 12):
    dp[i] = dp[i - 1] + dp[i - 2] + dp[i - 3]
for _ in range(n):
    idx = int(input())
    print(dp[idx])
